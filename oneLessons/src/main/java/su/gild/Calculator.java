package su.gild;

public class Calculator {

    public int plus(int left, int right) {
        return left + right;
    }

    public double plus(double left, double right) {
        return left + right;
    }

    public int minus(int left, int right) {
        return left - right;
    }

    public double minus(double left, double right) {
        return left - right;
    }

    public int multi(int left, int right) {
        return left * right;
    }

    public double multi(double left, double right) {
        return left * right;
    }

    public double divide(double left, double right) {
        return left / right;
    }
}
